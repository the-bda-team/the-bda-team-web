import sys
import json


def get_attribute_sort_key(attribute):
    attribute_name = attribute[0]
    if attribute_name == "id":
        return ""
    elif attribute_name == "category":
        return " "
    else:
        return attribute_name


def get_sort_key_default(item):
    return item["id"]


people_category_order = {
    "Teamlead": "1",
    "Staff": "2"
}


def get_sort_key_people(item):
    return f"{people_category_order[item['category']]} {item['id']}"


month_order = { "jan": "12", "feb": "11", "mar": "10", "apr": "09", "may": "08", "jun": "07", "jul": "06", "aug": "05", "sep": "04", "oct": "03", "nov": "02", "dec": "01" }


def get_sort_key_publications(item):
    year = 9999 - item["year"]
    venue = ""
    if "booktitle" in item:
        venue = item["booktitle"]
    elif "journal" in item:
        venue = item["journal"]
    return f"{year} {month_order[item['month']]} {venue} {item['id']}"


